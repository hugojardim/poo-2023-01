package com.github.hugojardim.ufg.poo.t10.t10;

public class Mochila extends Roupa{

    public double peso;

    public double tamanhoLitros;

    public String getCor() {
        return cor;
    }

    public void setCor(String cor) {
        this.cor = cor;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getTamanho() {
        return tamanho;
    }

    public void setTamanho(String tamanho) {
        this.tamanho = tamanho;
    }

    public double getPeso() {
        return peso;
    }

    public void setPeso(double peso) {
        this.peso = peso;
    }

    public double getTamanhoLitros() {
        return tamanhoLitros;
    }

    public void setTamanhoLitros(double tamanhoLitros) {
        this.tamanhoLitros = tamanhoLitros;
    }
}
